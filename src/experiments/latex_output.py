"""Generate LaTeX tables comparing baseline and graph-memory results."""
from __future__ import annotations

from typing import Dict, List, Optional

from experiments.baselines import (
    LOCOMO_BASELINES,
    LOCOMO_COLUMNS,
    LONGMEMEVAL_BASELINES,
    LONGMEMEVAL_COLUMNS,
    LONGMEMEVAL_COL_SHORT,
)


def _fmt(val: Optional[float], is_pct: bool = False, bold: bool = False) -> str:
    if val is None:
        return "--"
    if is_pct:
        s = f"{val:.1f}"
    else:
        s = f"{val:.3f}"
    return f"\\textbf{{{s}}}" if bold else s


# =====================================================================
# LoCoMo LaTeX table
# =====================================================================

def locomo_latex_table(
    our_results: List[Dict],
    caption: str = "LoCoMo QA Results (F1 Score)",
    label: str = "tab:locomo",
) -> str:
    """Generate a LaTeX table for LoCoMo results.

    *our_results*: list of dicts from ``run_locomo.evaluate_locomo``.
    """
    cat_map = {"Cat1": 1, "Cat2": 2, "Cat3": 3, "Cat4": 4, "Cat5": 5}
    n_cols = len(LOCOMO_COLUMNS)

    lines = [
        r"\begin{table}[ht]",
        r"\centering",
        r"\caption{" + caption + "}",
        r"\label{" + label + "}",
        r"\begin{tabular}{l" + "c" * n_cols + "}",
        r"\toprule",
        r"Method & " + " & ".join(
            [r"\textbf{" + c.replace("Cat", "Cat ") + "}" for c in LOCOMO_COLUMNS]
        ) + r" \\",
        r"\midrule",
    ]

    # Baseline rows
    for method, scores in LOCOMO_BASELINES.items():
        row = [method]
        for col in LOCOMO_COLUMNS:
            row.append(_fmt(scores.get(col)))
        lines.append(" & ".join(row) + r" \\")

    lines.append(r"\midrule")

    # Our rows
    for res in our_results:
        name = res["method"]
        row = [name]
        for col in LOCOMO_COLUMNS:
            cat_key = col
            if col == "Overall":
                val = res.get("overall")
            else:
                cat_num = cat_map[col]
                val = res.get("per_category", {}).get(cat_num)
            row.append(_fmt(val, bold=True))
        lines.append(" & ".join(row) + r" \\")

    lines += [
        r"\bottomrule",
        r"\end{tabular}",
        r"\end{table}",
    ]
    return "\n".join(lines)


# =====================================================================
# LongMemEval LaTeX table
# =====================================================================

def longmemeval_latex_table(
    our_results: List[Dict],
    caption: str = "LongMemEval QA Results (Accuracy \\%)",
    label: str = "tab:longmemeval",
) -> str:
    n_cols = len(LONGMEMEVAL_COLUMNS)
    short_headers = [LONGMEMEVAL_COL_SHORT.get(c, c) for c in LONGMEMEVAL_COLUMNS]

    lines = [
        r"\begin{table*}[ht]",
        r"\centering",
        r"\caption{" + caption + "}",
        r"\label{" + label + "}",
        r"\resizebox{\textwidth}{!}{",
        r"\begin{tabular}{l" + "c" * n_cols + "}",
        r"\toprule",
        r"Method & " + " & ".join(
            [r"\textbf{" + h + "}" for h in short_headers]
        ) + r" \\",
        r"\midrule",
    ]

    for method, scores in LONGMEMEVAL_BASELINES.items():
        row = [method]
        for col in LONGMEMEVAL_COLUMNS:
            row.append(_fmt(scores.get(col), is_pct=True))
        lines.append(" & ".join(row) + r" \\")

    lines.append(r"\midrule")

    for res in our_results:
        name = res["method"]
        row = [name]
        for col in LONGMEMEVAL_COLUMNS:
            if col == "Task-Avg":
                val = res.get("task_avg")
            elif col == "Overall":
                val = res.get("overall")
            elif col == "Abstention":
                val = res.get("abstention")
            else:
                val = res.get("per_type", {}).get(col)
            row.append(_fmt(val, is_pct=True, bold=True))
        lines.append(" & ".join(row) + r" \\")

    lines += [
        r"\bottomrule",
        r"\end{tabular}}",
        r"\end{table*}",
    ]
    return "\n".join(lines)


# =====================================================================
# Combined output
# =====================================================================

def generate_all_tables(
    locomo_results: List[Dict],
    longmemeval_results: List[Dict],
    output_path: Optional[str] = None,
) -> str:
    """Generate both tables and optionally write to a .tex file."""
    parts = [
        "% Auto-generated by selective-amnesia experiment suite",
        "% Requires: booktabs, graphicx packages",
        "",
        locomo_latex_table(locomo_results),
        "",
        longmemeval_latex_table(longmemeval_results),
    ]
    tex = "\n".join(parts)

    if output_path:
        from pathlib import Path
        Path(output_path).write_text(tex)
        print(f"LaTeX tables written to {output_path}")

    return tex
